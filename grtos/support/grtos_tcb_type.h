#ifndef __TCB_TYPE_H__
#define __TCB_TYPE_H__

#include "grtos_FreeRTOS.h"

/*
 * Task control block.  A task control block (TCB) is allocated for each task,
 * and stores task state information, including a pointer to the task's context
 * (the task's run time environment, including register values)
 */
struct task_control_block
{
	volatile StackType_t	*pxTopOfStack;	/*< Points to the location of the last item placed on the tasks stack.  THIS MUST BE THE FIRST MEMBER OF THE TCB STRUCT. */

	UBaseType_t			uxPriority;			/*< The priority of the task.  0 is the lowest priority. */
	StackType_t			*pxStack;			/*< Points to the start of the stack. */
	char				pcTaskName[ configMAX_TASK_NAME_LEN ];/*< Descriptive name given to the task when created.  Facilitates debugging only. */ /*lint !e971 Unqualified char types are allowed for strings and single characters only. */
	UBaseType_t state; /*< from the TASK_STATE values enumerated below. */

};

#define TASK_STATE_WAITING   0
/* Blocked waiting for an event */
#define TASK_STATE_SUSPENDED 1
/* Suspended so a higher priority task can be run */
#define TASK_STATE_RUNNING   2
/* Currently running */

#endif // __TCB_TYPE_H__
